#include<bits/stdc++.h>
using namespace std;

class rectangle{
    public:
    int length, breadth;
    rectangle(int x, int y)
    {
        length = x;
        breadth = y;
    }
    int area()
    {
        return (length * breadth);//return diye void mari disi hehe
    }
};

int main()//done
{
  rectangle a(4,5), b(5,8);

  cout << a.area() << endl;
  cout << b.area() << endl;



   return 0;
}
